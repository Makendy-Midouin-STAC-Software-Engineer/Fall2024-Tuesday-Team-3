const std::string FIREBASE_URL = "https://mazegame-5f110-default-rtdb.firebaseio.com/leaderboard.json";

// Function to send scores to Firebase
void sendScoreToFirebase(const std::string& playerName, float mazeTime, int mazeNumber) {
    CURL* curl;
    CURLcode res;

    Json::Value mazeUpdate;
    mazeUpdate["maze" + std::to_string(mazeNumber) + "Time"] = mazeTime;

    Json::StreamWriterBuilder writer;
    std::string jsonData = Json::writeString(writer, mazeUpdate);

    std::string firebaseUrl = FIREBASE_URL.substr(0, FIREBASE_URL.find(".json")) + "/" + playerName + ".json";

    curl = curl_easy_init();
    if (curl) {
        curl_easy_setopt(curl, CURLOPT_URL, firebaseUrl.c_str());
        curl_easy_setopt(curl, CURLOPT_CUSTOMREQUEST, "PATCH"); // Use PATCH to update only specific fields
        curl_easy_setopt(curl, CURLOPT_POSTFIELDS, jsonData.c_str());

        struct curl_slist* headers = NULL;
        headers = curl_slist_append(headers, "Content-Type: application/json");
        curl_easy_setopt(curl, CURLOPT_HTTPHEADER, headers);

        res = curl_easy_perform(curl);
        if (res != CURLE_OK) {
            std::cerr << "Failed to send data to Firebase: " << curl_easy_strerror(res) << std::endl;
        }

        curl_easy_cleanup(curl);
    }
}

// Callback function for fetching data
size_t writeCallback(void* contents, size_t size, size_t nmemb, std::string* userp) {
    userp->append((char*)contents, size * nmemb);
    return size * nmemb;
}

// Function to fetch leaderboard data from Firebase
void fetchLeaderboardFromFirebase(std::vector<LeaderboardEntry>& leaderboard) {
    CURL* curl;
    CURLcode res;

    std::string readBuffer;
    curl = curl_easy_init();
    if (curl) {
        curl_easy_setopt(curl, CURLOPT_URL, FIREBASE_URL.c_str());
        curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, writeCallback);
        curl_easy_setopt(curl, CURLOPT_WRITEDATA, &readBuffer);

        res = curl_easy_perform(curl);
        if (res != CURLE_OK) {
            std::cerr << "Failed to fetch data from Firebase: " << curl_easy_strerror(res) << std::endl;
        } else {
            Json::CharReaderBuilder reader;
            Json::Value jsonData;
            std::string errors;

            std::istringstream ss(readBuffer);
            if (Json::parseFromStream(reader, ss, &jsonData, &errors)) {
                leaderboard.clear();
                for (const auto& key : jsonData.getMemberNames()) {
                    LeaderboardEntry entry;
                    entry.name = jsonData[key].isMember("name") ? jsonData[key]["name"].asString() : key;
                    entry.maze1Time = jsonData[key]["maze1Time"].asFloat();
                    entry.maze2Time = jsonData[key]["maze2Time"].asFloat();
                    entry.maze3Time = jsonData[key]["maze3Time"].asFloat();
                    leaderboard.push_back(entry);
                }
            } else {
                std::cerr << "Failed to parse JSON: " << errors << std::endl;
            }
        }

        curl_easy_cleanup(curl);
    }
}


